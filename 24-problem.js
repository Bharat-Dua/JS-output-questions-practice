String.prototype.youAreAmazing = () => {
  return "You are amazing my friend";
};

const who = "Bharat";
console.log(who.youAreAmazing()); // Output: You are amazing my friend

//* Prototype:- Prototype is a key,that exists on every object and function and its value of prototype is object.and we use prototype to share common functionality among object instances.
