const data1 = ["C", "B", "A"];
const data2 = ["Z", "Y", "X"];

data1.toReversed(); // The toReversed() method in JavaScript creates a reversed copy of an array without altering the original array.
data2.reverse();

console.log({ data1 }); // { data1: [ 'C', 'B', 'A' ] }
console.log({ data2 }); // { data2: [ 'X', 'Y', 'Z' ] }
